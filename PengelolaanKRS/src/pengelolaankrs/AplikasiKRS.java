    private void textTeoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textTeoriActionPerformed
         
        try {
        sksPraktik  = Integer.parseInt(textPraktikum.getText());
        sksTeori = Integer.parseInt(textTeori.getText());
        totalSks = sksPraktik+sksTeori;
            cekSKS(totalSks);
        }catch (sksOver e){
            JOptionPane.showMessageDialog(this,"Jumlah SKS melebihi standar");
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Input Dengan Angka yang benar");
        }
        
        
        
        if(comboJurusan.getSelectedIndex()==0)
        {
            
        }else if(comboJurusan.getSelectedIndex()==1)
        {
            hargaSKS = 175000;
        }else if(comboJurusan.getSelectedIndex()==2)
        {
            hargaSKS = 185000;
        }else if(comboJurusan.getSelectedIndex()==3)
        {
            hargaSKS = 195000;
        }
        totalBayar=totalSks*hargaSKS;
        tempTotal=String.valueOf(totalBayar);
        textTotal.setText(tempTotal);
    }//GEN-LAST:event_textTeoriActionPerformed

