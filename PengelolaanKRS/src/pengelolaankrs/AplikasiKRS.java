    private void textIPKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textIPKActionPerformed
        ipk= (int) Double.parseDouble(textIPK.getText());
        if(ipk<=4 && ipk >= 3.5)
        {   
            sks =24;
        }else if(ipk <= 3.4 && ipk >=3)
        {
            sks =23;
        }else if(ipk <=2.9 && ipk >= 2.5)
        {
            sks=22;
        }
        Temp = String.valueOf(sks);
        textSKS.setText(Temp);
    }//GEN-LAST:event_textIPKActionPerformed

